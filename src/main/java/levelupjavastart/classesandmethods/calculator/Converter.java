package levelupjavastart.classesandmethods.calculator;

public class Converter {
    public static class IntAAndB {
        int a;
        int b;

        IntAAndB(int a, int b) {
            this.a = a;
            this.b = b;
        }
    }

    public static class DoubleAAndB {
        double a;
        double b;

        DoubleAAndB(double a, double b) {
            this.a = a;
            this.b = b;
        }
    }

    public static class LongAAndB {
        long a;
        long b;

        LongAAndB(long a, long b) {
            this.a = a;
            this.b = b;
        }
    }
}
